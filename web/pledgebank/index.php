<h2>How does it work?</h2>

<p>PledgeBank is about reassuring people that they won&#8217;t be alone if they
decide to do something altruistic for their community. It works by letting
people make pledges of the form &#8220;I will do something, but only if 10 other
people will do it too&#8221;. Launched in June 2005 it has a remarkable first 6
months, picking up over 30,000 signatures on over 1000 pledges, and achieving
goals from getting over 100 people to give blood, to creating sustainable
funding streams for new and pre-existing community groups in the UK and the
US.</p>

<h2>How do you know people keep their pledges?</h2>

<p>PledgeBank relies on a calculation about human psychology. Instead of tying
our users into unenforceable contracts, we built the site to push already
somewhat motivated people into action. To test whether this had been the
correct assessment of the way people think, we asked successful pledge creators
who&#8217;d asked for money in their pledges how much they actually got. The worst
result was 50%, the best about 150%, most achieving about 70&ndash;80% of what they&#8217;d
asked for.</p>

