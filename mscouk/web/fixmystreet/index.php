<h1>Fix My Street</h1>

<p id="sub">Encourages citizens to report problems instead of passing them by</p>

<h2>Benefits</h2>

<img class="right" align="right" src="fixmystreet.png" alt="">
<ul>
<li>Zero integration required into council front or back end systems
<li>Easy to use interface increases reporting
<li>Connects citizens with mutual local concerns with one another
<li>Option to run on an independent site, or brand and run on council site
<li>Compatible with all council street maintenance systems
<li>Rapid installation (less than 1 week)
</ul>

<h3>How does it work?</h3>

<p>A user who sees something broken in their own local area visits the site,
sticks a virtual pin into the map and enters a short description of what&#8217;s
wrong. On hitting &#8216;submit&#8217; the site works out what sort of problem they have
encountered, and emails the relevant part of the appropriate local council with
a copy of the report. The council can then enter the data easily into their own
system, and with one click report the status as changed both on the map, and in
an email to the problem reporter.</p>

<h3>What need does it address?</h3>

<p>Reporting and complaining about broken paving slabs, fly tipping and other
public problems is at present a private process between complainer and council.
This is strange when the problems themselves are public, and affect many
people.</p>

<p>This product&#8217;s purpose is to allow constituents to understand if anyone else
has already reported a problem, and to see how the council is acting on it.</p>

