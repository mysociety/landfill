<h1>GiveItAway</h1>

<p id="sub">Reduce landfill whilst helping charities and community groups</p>

<h2>Benefits</h2>

<ul>
<li>Diverts unwanted goods which would otherwise be thrown away to groups that can use or resell them.
<li>Service integrated into waste and recycling pages of council websites
<li>No back end integration required
<li>Leverages value in council maintained lists of charities and community groups, making investment already made in these more efficient.
</ul>

<h2>How does it work?</h2>

<p>GiveItAway adds a new, prominent box to the waste and recycling pages of a council.s website. The new box reads:</p>

<blockquote>
<p>&#8220;Stop &mdash; before you throw it away, why not try giving it away to a local charity
or community group?&#8221;</p>
</blockquote>

<p>Beneath this message is a very small, simple interface which asks for a user&#8217;s
email address, their postcode, and the approximate type of item (i.e. books,
clothes, electronics). When they have filled in this information they hit
enter, and the site tells them to check their inbox.</p>

<p>In their email they will find an email, CCed to them, to the nearest
charity or community group to where they live. The email is addressed to the
group and says:</p>

<blockquote>
<p>&#8220;Hello there,</p>
<p>A user of your council website has some books that they are interested in
giving to a good cause. Would these be of interest to you? If so, please just
hit reply and you can ask the person in question what they are.&#8221;</p>
</blockquote>

<p>The donor and the group then discuss via email whether the items are
appropriate, and how to follow up. The goal is to increase the proportion of
worthwhile goods which end up in the hands of charities and community groups,
and not in landfills.</p>

